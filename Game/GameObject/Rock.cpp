Rock::Rock() { Init(); }
Rock::~Rock() {}

//////////////////////////////////////////////////////
//  初期化処理
//////////////////////////////////////////////////////

void Rock::Init() {

	typeID_ = (int)ObjectType::ROCK;

	BaseGameObject::Init();
	SetObject("Rock.obj");

	obb_.size = { 1.0f, 1.0f, 1.0f };
	obb_.center = GetWorldTranslation();
}

//////////////////////////////////////////////////////
//  更新処理
//////////////////////////////////////////////////////

void Rock::Update() {
	obb_.center = GetWorldTranslation();
	obb_.MakeOBBAxis(transform_->GetQuaternion());
	BaseGameObject::Update();
}

