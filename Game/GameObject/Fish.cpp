#include "Fish.h"

//////////////////////////////////////////////////////
//  コンストラクタ・デストラクタ
//////////////////////////////////////////////////////

Fish::Fish(){ Init(); }
Fish::~Fish(){}

//////////////////////////////////////////////////////
//  初期化処理
//////////////////////////////////////////////////////

void Fish::Init(){

	typeID_ = (int)ObjectType::FISH;
	
	BaseGameObject::Init();
	radius_ = 1.0f;
	SetObject("fish.obj");
}

//////////////////////////////////////////////////////
//  更新処理
//////////////////////////////////////////////////////

void Fish::Update(){
	BaseGameObject::Update();
}

//////////////////////////////////////////////////////
//  描画処理
//////////////////////////////////////////////////////

void Fish::Draw() const{
	BaseGameObject::Draw();
}

//////////////////////////////////////////////////////
//  衝突時処理
//////////////////////////////////////////////////////

void Fish::OnCollision(Collider* other){

	//プレイヤーに当たった場合
	if(other->GetObjectType() == int(ObjectType::PLAYER)){
		isActive_ = false;
	}
}
