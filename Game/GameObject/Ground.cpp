#include "Ground.h"

Ground::Ground() {
	Init();
}

Ground::~Ground() {
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　初期化処理
//////////////////////////////////////////////////////////////////////////////////////////////////

void Ground::Init() {
	BaseGameObject::Init();
	SetObject("Riverbed1.obj");
	for (uint32_t oi = 0; oi < materials.size(); ++oi) {
		materials[oi]->SetUvScale({ 1.0f, 1.0f, 1.0f });
	}
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　更新処理
//////////////////////////////////////////////////////////////////////////////////////////////////

void Ground::Update() {
	BaseGameObject::Update();
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　描画処理
//////////////////////////////////////////////////////////////////////////////////////////////////

void Ground::Draw() const {
	BaseGameObject::Draw();
}


//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　debug表示
//////////////////////////////////////////////////////////////////////////////////////////////////

#ifdef _DEBUG
void Ground::Debug_Gui() {
	ImGui::Begin("Ground");
	transform_->Debug_Gui();
	for (uint32_t oi = 0; oi < materials.size(); ++oi) {
		materials[oi]->ImGuiDraw();
	}
	ImGui::End();
}
#endif // _DEBUG