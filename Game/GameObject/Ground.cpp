#include "Ground.h"

Ground::Ground() {
	Init();
}

Ground::~Ground() {
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　初期化処理
//////////////////////////////////////////////////////////////////////////////////////////////////

void Ground::Init() {
	BaseGameObject::Init();
	SetObject("ground.obj");
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　更新処理
//////////////////////////////////////////////////////////////////////////////////////////////////

void Ground::Update() {
	Move();
	BaseGameObject::Update();
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　描画処理
//////////////////////////////////////////////////////////////////////////////////////////////////

void Ground::Draw() const {
	BaseGameObject::Draw();
}

void Ground::Move() {
	Vector3 translate = transform_->GetTranslation();
	translate.x += playerVelocityX_ * GameTimer::DeltaTime();
	transform_->SetTranslaion(translate);
}

