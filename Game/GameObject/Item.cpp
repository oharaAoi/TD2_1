#include "Item.h"

//////////////////////////////////////////////////////
//  コンストラクタ・デストラクタ
//////////////////////////////////////////////////////

Item::Item(){ Init(); }
Item::~Item(){}

//////////////////////////////////////////////////////
//  初期化処理
//////////////////////////////////////////////////////

void Item::Init(){

	typeID_ = (int)ObjectType::ITEM;

	BaseGameObject::Init();
	SetObject("skin.obj");

	obb_.size = { 1.0f, 1.0f, 1.0f };
	obb_.center = GetWorldTranslation();
	SetColor({1.0f, 1.0f, 0.0f, 1.0f});
}

//////////////////////////////////////////////////////
//  更新処理
//////////////////////////////////////////////////////

void Item::Update(){
	obb_.center = GetWorldTranslation();
	obb_.MakeOBBAxis(transform_->GetQuaternion());
	BaseGameObject::Update();
}

//////////////////////////////////////////////////////
//  描画処理
//////////////////////////////////////////////////////

void Item::Draw() const{
	BaseGameObject::Draw();
}

//////////////////////////////////////////////////////
//  衝突時処理
//////////////////////////////////////////////////////

void Item::OnCollision(Collider* other){

	//プレイヤーに当たった場合
	if(other->GetObjectType() == int(ObjectType::PLAYER)){
		isActive_ = false;
	}
}

void Item::IndividualFromCommon(const SubAttributeType& subType) {
	switch (subType) {
	case SubAttributeType::NONE:
		break;
	default:
		break;
	}
}

