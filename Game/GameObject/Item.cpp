#include "Item.h"

//////////////////////////////////////////////////////
//  コンストラクタ・デストラクタ
//////////////////////////////////////////////////////

Item::Item(){ Init(); }
Item::~Item(){}

//////////////////////////////////////////////////////
//  初期化処理
//////////////////////////////////////////////////////

void Item::Init(){

	typeID_ = (int)ObjectType::ITEM;

	BaseGameObject::Init();
	SetObject("Item.obj");
}

//////////////////////////////////////////////////////
//  更新処理
//////////////////////////////////////////////////////

void Item::Update(){
	BaseGameObject::Update();
}

//////////////////////////////////////////////////////
//  描画処理
//////////////////////////////////////////////////////

void Item::Draw() const{
	BaseGameObject::Draw();
}

//////////////////////////////////////////////////////
//  衝突時処理
//////////////////////////////////////////////////////

void Item::OnCollision(Collider* other){

	//プレイヤーに当たった場合
	if(other->GetObjectType() == int(ObjectType::PLAYER)){
		isActive_ = false;
	}
}

