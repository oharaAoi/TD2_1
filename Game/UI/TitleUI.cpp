#include "TitleUI.h"

TitleUI::TitleUI() {
}

TitleUI::~TitleUI() {
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　
//////////////////////////////////////////////////////////////////////////////////////////////////

void TitleUI::Init() {
	UI_title_ = Engine::CreateSprite("title.png");
	UI_startKeyUI_ = Engine::CreateSprite("kari.png");

	UI_title_->SetTextureCenterPos({640.0f, 360.0f});
	UI_startKeyUI_->SetTextureCenterPos({640.0f, 500.0f});
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　
//////////////////////////////////////////////////////////////////////////////////////////////////

void TitleUI::Update() {
	UI_title_->Update();
	UI_startKeyUI_->Update();
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　
//////////////////////////////////////////////////////////////////////////////////////////////////

void TitleUI::Draw() const {
	UI_title_->Draw();
	UI_startKeyUI_->Draw();
}

